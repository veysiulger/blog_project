<?php
class Users{

    private $userName;
    private $userID;
    private $userEmail;
    private $userPassword;

    private function __construct($userName,$userID,$userEmail,$userPassword){

        $this->userName=$userName;
        $this->userID=$userID;
        $this->userEmail=$userEmail;
        $this->userPassword=$userPassword;
    }





}
